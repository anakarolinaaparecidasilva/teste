package condicionais;

public class OperadoresRelacionais {
    public static void main(String[] args) {

        int i1 = 1;
        int i2 = 21;
        float f1 = 2.1f;
        float f2 = 5.4f;
        double d1 = 59.6d;
        char c1 = 'x';
        char c2 = 'i';
        String s1 = "Ana";
        String s2 = "Karolina";
        boolean bl1 = true;
        boolean bl2 = false;

        long l1 = 15971;
        long l2 = 87898;
        byte b1 = 1;
        byte b2 = 89;

        System.out.println("i1 == i2 " + (i1 == i2));
        System.out.println("i1 >= i2 " + (i1 >= i2));
        System.out.println("i1 <= i2 " + (i1 <= i2));
        System.out.println("i1 != i2 " + (i1 == i2));
        System.out.println("i1 > i2 " + (i1 == i2));
        System.out.println("i1 < i2 " + (i1 == i2));
    }

}
